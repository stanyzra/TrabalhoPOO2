    private void botaoMenuClientesMesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoMenuClientesMesActionPerformed
        // TODO add your handling code here:
        opcoesMed.show(cardOpcoesMedico, "gerarRelClientesMes");
        mesField.setText(String.valueOf(LocalDate.now().getMonthValue()));
        pacientes = med.consultarPacientes();
        for (int i = 0; i < pacientes.size(); i++) {
            if (pacientes.get(i).isConsultaCadastrada() && pacientes.get(i).getConsulta().getLocalDateCons().getMonthValue() != LocalDate.now().getMonthValue()){
                pacientes.remove(i);
                i--;
            }else if (!pacientes.get(i).isProntuarioCadastrado() && !pacientes.get(i).isDadosAdicionaisCadastrado()){
                pacientes.remove(i);
                i--;
            }
        }
        atualizarListaClientesMes();
    }//GEN-LAST:event_botaoMenuClientesMesActionPerformed

    private void botaoCadastraDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastraDadosActionPerformed

        ///////////////////////////////////////////////////////////
        //                    CADASTRA DADOS                    //
        /////////////////////////////////////////////////////////        
        
        
        DadosAdicionais dadoNovo = new DadosAdicionais();
        
        
        if(cadastraFumanteCheck.isSelected()) dadoNovo.setFuma(true);
        else dadoNovo.setFuma(false);
        if(cadastraBebidaCheck.isSelected()) dadoNovo.setBebe(true);
        else dadoNovo.setBebe(false);
        if(cadastraColesterolCheck.isSelected()) dadoNovo.setColesterol(true);
        else dadoNovo.setColesterol(false);
        if(cadastraDiabetesCheck.isSelected()) dadoNovo.setDiabete(true);
        else dadoNovo.setDiabete(false);
        if(cadastraCardiacoCheck.isSelected()) dadoNovo.setDoencaCard(true);
        else dadoNovo.setDoencaCard(false);
        
        dadoNovo.setCirurgias(cadastraCirurgiasField.getText());
        dadoNovo.setAlergias(cadastraAlergiaField.getText());
        
        pacientes.get(pacientesDadosBox.getSelectedIndex()).setDadosAdicionais(dadoNovo);
        dadoNovo.setPaciente(pacientes.get(pacientesDadosBox.getSelectedIndex()));
        dadoNovo.getPaciente().setDadosAdicionaisCadastrado(true);

        med.cadastrarDadosPacientes(dadoNovo);
        med.atualizarPaciente(pacientes.get(pacientesDadosBox.getSelectedIndex()));
        JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
        pacientes.remove(pacientesDadosBox.getSelectedIndex());
        pacientesDadosBox.removeItemAt(pacientesDadosBox.getSelectedIndex());
        
        limparCampos(getContentPane());
        atualizarComboBoxPacDados();
    }//GEN-LAST:event_botaoCadastraDadosActionPerformed

