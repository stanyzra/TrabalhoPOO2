    private void botaoAtualizarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarDadosActionPerformed
        ///////////////////////////////////////////////////////////
        //                  ATUALIZA DADOS                      //
        /////////////////////////////////////////////////////////
        
        DadosAdicionais dadosAtualizados = new DadosAdicionais();

        if(atualizaFumanteCheck.isSelected()) dadosAtualizados.setFuma(true);
        else dadosAtualizados.setFuma(false);
        if(atualizaBebidaCheck.isSelected()) dadosAtualizados.setBebe(true);
        else dadosAtualizados.setBebe(false);
        if(atualizaColesterolCheck.isSelected()) dadosAtualizados.setColesterol(true);
        else dadosAtualizados.setColesterol(false);
        if(atualizaDiabetesCheck.isSelected()) dadosAtualizados.setDiabete(true);
        else dadosAtualizados.setDiabete(false);
        if(atualizaCardiacoCheck.isSelected()) dadosAtualizados.setDoencaCard(true);
        else dadosAtualizados.setDoencaCard(false);
        dadosAtualizados.setCirurgias(atualizaCirurgiasField.getText());
        dadosAtualizados.setAlergias(atualizaAlergiasField.getText());
        
        dadosAtualizados.setIdDadosAdicionais(dadosAdicionais.get(atualizaDadosPacientesBox.getSelectedIndex()).getIdDadosAdicionais());
        dadosAtualizados.setPaciente(dadosAdicionais.get(atualizaDadosPacientesBox.getSelectedIndex()).getPaciente());
      
        med.atualizarDadosPaciente(dadosAtualizados);
        JOptionPane.showMessageDialog(null, "Atualização realizado com sucesso");
        //pacientes.remove(pacientesDadosBox.getSelectedIndex());
        
        atualizarComboBoxPacAttDados();
        atualizarCamposAlteracaoDados();
        
    }//GEN-LAST:event_botaoAtualizarDadosActionPerformed

    private void botaoRemoverDadosAdcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverDadosAdcActionPerformed
        // TODO add your handling code here:
        dadosAdicionais = med.consultarDadosAdicionais();
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja mesmo remover estes dados?", "Remoção", 1);
        if(resposta == 0){
            //int indexDados = dadosAdicionais.get(listaDados.getSelectedIndex()).getIdDadosAdicionais();
            //DadosAdicionais dados = med.consultarUmDado(indexDados);
            DadosAdicionais dados = dadosAdicionais.get(listaDados.getSelectedIndex());
            dadosAdicionais.get(listaDados.getSelectedIndex()).getPaciente().setDadosAdicionaisCadastrado(false);
            dadosAdicionais.get(listaDados.getSelectedIndex()).getPaciente().setDadosAdicionais(null);
            med.atualizarPaciente(dadosAdicionais.get(listaDados.getSelectedIndex()).getPaciente());
            
            med.removerDadosPaciente(dados);
            
            JOptionPane.showMessageDialog(null, "Dado removido");
            atualizarListaRemoverDados();
        }else if (resposta == 1){
            JOptionPane.showMessageDialog(null, "Dado mantida");
        }

    }//GEN-LAST:event_botaoRemoverDadosAdcActionPerformed

